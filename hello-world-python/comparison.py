# < Less than
# <= Less than or equal too
# == Equal to
# <= Greater than or equal to
# > Greater than
# != Not Equal

x = 5

if x == 5:
  print("Equals 5")

if x > 4:
  print("Greater Than 4")

if x >= 5:
  print("Greater than or equal 5")

if x <= 5:
  print("Less Than or Equal to 5")

if x != 6:
  print("Not Equal 6")

